    private void ponerImagen(JLabel lbl, String ruta) {
        this.imagen = new ImageIcon(ruta);
        this.icono = new ImageIcon(this.imagen.getImage().getScaledInstance(lbl.getWidth(), lbl.getHeight(), Image.SCALE_DEFAULT));
        lbl.setIcon(this.icono);
        this.repaint();

    }

    String informacion = "";
    double totalFinal = 0;

    public void clean() {

        this.txt_nombre.setText("");
        this.cmb_CantidadDeBoletos.setSelectedIndex(0);
        //this.txt_TotalAPagar.setText("");

    }

    public void getNombre() {

        int boletos;
        double total;

        boletos = this.cmb_CantidadDeBoletos.getSelectedIndex() + 1;
        total = getPrecio();

        totalFinal = totalFinal + total;

        this.txt_TotalAPagar.setText(String.valueOf(df.format(totalFinal)));

        informacion += "- " + this.txt_nombre.getText().trim() + " \n boletos: " + String.valueOf(boletos) + " \nTotal: $" + String.valueOf(total) + "\n";

        this.txt_NombresPersonas.setText(informacion);

    }

    public double getPrecio() {

        double precio = 0, descuento = 0;
        int boletos;

        boletos = this.cmb_CantidadDeBoletos.getSelectedIndex() + 1;

        precio = boletos * 12;

        if (boletos > 5) {

            descuento = precio * 0.15;
            precio = precio - descuento;

        } else if (boletos > 2 && boletos <= 5) {

            descuento = precio * 0.10;
            precio = precio - descuento;

        }

        descuento = 0;

        if (this.rb_Si.isSelected()) {

            descuento = precio * 0.10;
            precio = precio - descuento;

        }

        return precio;

    }

